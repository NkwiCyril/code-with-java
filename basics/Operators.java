package basics;

public class Operators {

    public static void main(String[] args) {
        int num1 = 3 + 3; // addition
        int num2 = 23 * 4; // multiplication
        int num3 = 23 / 2; // division
        int num4 = 43 % 7; // modulus, return the remainder of the division between both ints
        int var = 23;
        int newVar = ++var; // increments var by 1;

        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(newVar);
    };
};
